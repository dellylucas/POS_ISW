package Modelo;

public class Caja {
	private Float totalDinero;
	private String devoluciones;
	private String cancelaciones;
	
	
	public Float getTotalDinero() {
		return totalDinero;
	}
	public void setTotalDinero(Float totalDinero) {
		this.totalDinero = totalDinero;
	}
	public String getDevoluciones() {
		return devoluciones;
	}
	public void setDevoluciones(String devoluciones) {
		this.devoluciones = devoluciones;
	}
	public String getCancelaciones() {
		return cancelaciones;
	}
	public void setCancelaciones(String cancelaciones) {
		this.cancelaciones = cancelaciones;
	}

}
